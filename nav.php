<!--Searchbox-->
<script>
    //Search box appears on click
    $(document).ready(function() {
        $(".search-open").hide();
        $("#search").show();
        $("#search").click(function() {
            $(".search-open").toggle();
        });
    });

</script>

<script>
    $(document).ready(function() {
        $("#search").click(function() {
            //show the black background
            $(".search-bk").css('display', 'block');
            //avoid scroll on the back
            $("body").toggleClass('no-scroll');
            //make sure close search is hide
            $(".close-search").hide();
            //close search appears
            $(".close-search").delay(500).fadeIn(300);

        });
    });

</script>

<script>
    //autofocus only on desktop
    $(document).ready(function() {
        if (navigator.userAgent.match(/(Macintosh|Windows)/i)) {
            $("#search").click(function() {
                $("#edit-search-block-form--2").focus();
            });
        }
    });

</script>

<script>
    $(document).ready(function() {
        $(".close-search").click(function() {
            //hide the black background
            $(".search-bk").css('display', 'none');
            //remove noscroll
            $("body").toggleClass('no-scroll');
            //hide searchbox
            $(".search-open").toggle();

        });
    });

</script>

